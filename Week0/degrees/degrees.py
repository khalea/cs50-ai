def shortest_path(source, target):
    """
    Returns the shortest list of (movie_id, person_id) pairs
    that connect the source to the target.
    If no possible path, returns None.

    # Iterate through source neighbors
    # Check if neighbors == target
    # If not, add to frontier
    # Once at the end of sourceNeighbors, 
    # recursively check neighbors of frontier members 
    # & remove after examining all of their neighbors
    """
    
    # Path list contains tuples (movieID, personID)
    path = []
    # Visited personIDs
    visited = []
    # Frontier contains personIDs for actors  
    frontier = QueueFrontier()
    # Source to front of queue
    frontier.add(source)
    # List of tuples (movieID, personID)
    sourceNeighbors = neighbors_for_person(source) 

    print("Source: " + source + " Target: " + target)
    
    # Go through neighbors & check for targetID
    # Add all to frontier if targetID isn't there
    for t in sourceNeighbors:
        if t[1] not in visited:
            if t[1] != target:
                frontier.add(t[1])
            else:
                path.append(t) 
                return path
    
    # Remove source from queue & add to visited
    # Don't have to add sources to path
    visited.append(frontier.remove())

    # Search through the rest of the frontier
    # Excluding visited nodes
    while frontier.empty() == False:
        person = frontier.remove()
        nb = neighbors_for_person(person)
        for p in nb:
            if p[1] not in visited:
                if p[1] != target:
                    frontier.add(p[1])
                else:
                    path.append(p) 
                    return path

    
         

            
    
